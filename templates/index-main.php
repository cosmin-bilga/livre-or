    <h1>Livre d'or</h1>
    <p>Un livre d’or est un registre ou un carnet mis à disposition lors d’un événement (mariage, exposition, funérailles, inauguration, etc.) ou dans un lieu public (musée, hôtel, restaurant, entreprise, etc.), afin de permettre aux visiteurs, invités ou clients d’y laisser un message, une impression, un témoignage ou un remerciement.</p>
    <p>Il peut contenir :</p>
    <ul>
        <li>Des mots de remerciement ou de félicitations.</li>
        <li>Des souvenirs partagés.</li>
        <li>Des commentaires sur une expérience vécue.</li>
        <li>Des vœux ou pensées adressés à un hôte ou une personne célébrée.</li>
    </ul>
